#include <stdio.h>
#include <stdlib.h>
int main()
{
    srand(time(0));
    int number = rand()%100+1;//[0-99]
    int a = 0;
    int count = 0;
    printf("我已经想好了一个1-100之间的数。",a);
    
    do{
       printf("请猜这个1到100之间的数：");
       scanf("%d",&a);
       count++;
       if(a>number){
          printf("你猜的数大了"); 
       }else if(a<number){
          printf("你猜的数小了");
       }                                   
    }while(a!=number); 
    printf("太好了，你用了%d次就猜到了答案。\n",count);
    system("pause");
    
    return 0;
}
