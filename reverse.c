#include <stdlib.h>
#include <stdio.h>

int main(){
    int x;
    scanf("%d",&x);
    int digit;
    int ret = 0;
    
    while(x>0){
       digit = x%10;//得到最右边的一位 
       printf("%d\n",digit);
       ret = ret*10+digit;
       printf("x=%d,digit=%d,ret=%d\n",x,digit,ret);
       x/=10;//丢掉最右边的一位 
    }
    printf("%d",ret);
    system("pause");
    return 0;
}
